def add(a, b):
  add_res = a + b
  return add_res

def subtract(a, b):
  sub_res = a - b
  return sub_res

def multiply(a, b):
  mult_res = a * b
  return mult_res

def divide(a, b):
  div_res = a / b
  return div_res

def exp(a, b):
  exp_res = a ** b
  return exp_res



