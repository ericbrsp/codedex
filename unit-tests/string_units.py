

def reverse_string(s):
  return s[::-1] # técnica de fatiamento que inverte a string

def capitalize_string(s):
  return s.capitalize() # retorna uma string com a primeira letra maiuscula e o restante em minúsculo

def is_capitalized(s):
   return s[0].isupper() # pega o primeiro caractere e verifica se está em maiúscula




